type LuckPrizeBase = {
  id: string;
  名称: string;
  出货概率: number;
  品阶: string;
  描述: string;
  数量?: number;
};

type LuckCombatPrize = LuckPrizeBase & {
  分类: '武器' | '装备' | '法宝';
  固定加成?: string[];
  效果?: string[];
};

type LuckConsumablePrize = LuckPrizeBase & {
  分类: '丹药' | '阵符';
  效果?: string[];
};

type LuckWearPrize = LuckPrizeBase & {
  分类: '着装';
  着装类型: string;
  固定加成?: string[];
  效果?: string[];
};

type LuckArtPrize = LuckPrizeBase & {
  分类: '功法';
  固定加成?: string[];
  招式: Array<{ 名称: string; 描述: string; 效果: string[] }>;
};

type LuckSpecialPrize = LuckPrizeBase & {
  分类: '特殊';
  内容?: string[];
};

export type LuckPrizeOption =
  | LuckCombatPrize
  | LuckConsumablePrize
  | LuckWearPrize
  | LuckArtPrize
  | LuckSpecialPrize;

